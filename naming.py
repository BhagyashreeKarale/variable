# How to name Variables?
# It is very important to give a correct name to a Variable.

# If you don't give names properly, then nobody will be able to read your Code, 
# as they won't be able to understand that for what purpose the variable is used?

# camelCase PascalCase snake_case UPPER_CASE kebab-case are five most popular cases for naming.

# You can read in detail about the cases here.

# In Python, we usually use:

# What Name?	                  Which Case?
# variableNames	                  camelCase
# CONSTANT_NAMES	              UPPER_CASE
# function_names	              snake_case

# Also, to give names to a variable is a skill in itself. 
# Read this article to understand about naming conventions.

# Category	               Suggested_Rule	                   Examples
# arrayVariables``	    usePlurals	                            fruits, vegetables, etc
#                                                               fruits = ["apple", "banana", "guavas"]
# booleanVariables``	start with is, can or has	            isOpen = True
#                                                               canWrite = False
#                                                               hasFruit = True
# function_names 	    use a verb or noun	                    get_user(userId)
#                                                               calculate_total(items)
#                                                               to_Dollars('euros', 20)
